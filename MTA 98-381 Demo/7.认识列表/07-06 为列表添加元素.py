sales_01=[56,60,78,38,100,110]
sales_02=[50,55,51,79,92,100,85]

#append方法,在列表末尾添加元素
sales_01.append(85)
#打印 [56, 60, 78, 38, 100, 110, 85]
print(sales_01)

#insert方法,在列表指定下标添加元素
sales_01.insert(2,80)
#打印 [56, 60, 80, 78, 38, 100, 110, 85]
print(sales_01)

#extend方法,扩展列表
#如果使用append，最后一个元素会成为嵌套的列表
sales_01.extend(sales_02)
#打印 [56, 60, 80, 78, 38, 100, 110, 85, 50, 55, 51, 79, 92, 100, 85]
print(sales_01)